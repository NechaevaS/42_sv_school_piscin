#include <unistd.h>

int main()
{
	char *str;
	int i;

	str = "0123456789";
	i = 9;
	while (i >= 0)
	{
		write(1, &str[i], 1);
		i--;
	}
	write (1, "\n", 1);
	return (0);
}
