#include <unistd.h>

int is_digit(char c)
{
	return (c >= '0' && c <= '9');
}

void	ft_print_numbers(void)
{
	char d;

	d = '0';
	while (is_digit(d))	
	{
		write(1, &d, 1);
		d++;
	}
}
